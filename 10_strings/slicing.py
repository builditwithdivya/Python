name = "Divya0123456789"

print(name[0:3])  # first three characters
print(name[1:4])  # second to fourth character
print(name[2:])  # from third character to end
print(name[:3])  # from start to third character

print(name[2:-1]) # same as name[2:4]

# print(name[2:4:n]) skip n-1 characters
print(name[2:10:1]) 
print(name[2:10:2])  # skip 1 character
print(name[2:10:3])  # skip 2 characters